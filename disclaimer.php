 что им отдельное спасибо.
 Но если цитата записана не составителем лично, то она может быть подвергнута проверке.
 </dd>
 <dt>А здесь точно цитаты?</dt>
 <dd>Точно. И, как правило, дословно. Допускается корректировка высказываний, которая не
 влияет на смысл цитаты, но приводит её к хоть каким-нибудь правилам русского или 
 английского языка. Если Вы заметили неточность, ошибку или опечатку, 
 прошу <a href="mailto:<?=$admin_email?>?subject=qs">сообщить</a> мне об этом. 
 </dd>
 <dt>За нами следят?</dt>
 <dd>Нет. Здесь не публикуются: диалоги тет-а-тет (только если собеседник сам не укажет 
