 составителю. Так что если Вы будете со мной после работы пить чай в парке, будьте 
 спокойны: публикуется только то, что слышал не только я один. 
 </dd>
 <dt>Удали это!</dt>
 <dd>Во-первых, не захотел бы, не опубликовывал цитату вообще. Во-вторых, если в Вашей
